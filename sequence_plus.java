import java.util.*;

public class JavaPractice3{
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    int i,sum=0;
    ArrayList<Integer> arr_num=new ArrayList<>();
    
    i=sc.nextInt();    
    
    while(i!=0){        
        arr_num.add(sc.nextInt());      
        i--;
    }
    
    for(i=0;i<arr_num.size();i++){
        sum+=arr_num.get(i);
    }
            
    System.out.println(sum);
    
  }
}
